    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        String kd_gr = tbl_guru.getValueAt(tbl_guru.getSelectedRow(),1).toString();
        System.out.println(kd_gr);
        String sqlDelete = "DELETE FROM guru WHERE kd_guru = ?";
        Config con = new Config();
        con.dbConnect();
        
        try{
            PreparedStatement psDelete = Config.conn.prepareStatement(sqlDelete);
            psDelete.setString(1, kd_gr);
            psDelete.execute();
            JOptionPane.showMessageDialog(null, "Data Sukses dihapus");
            guru gr = new guru();
            gr.show();
            dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Data Gagal dihapus");
            guru gr = new guru();
            gr.show();
            dispose();
        }
        
        
    }//GEN-LAST:event_btnHapusActionPerformed
    
    public void table(){
        Config con = new Config();
        String query = "SELECT * FROM guru";
        con.dbConnect();
        
        try {
            stmt = Config.conn.createStatement();
            resultSet = stmt.executeQuery(query);
            int i = 1;
            while(resultSet.next()){
                String kd_guru = resultSet.getString("kd_guru");
                String nama_guru = resultSet.getString("nama_guru");
                String alamat = resultSet.getString("alamat");
                String no_tlp = resultSet.getString("no_tlp");
                String columns[] = {String.valueOf(i++),kd_guru, nama_guru, alamat,no_tlp };
                
                DefaultTableModel tblModel = (DefaultTableModel)tbl_guru.getModel();
                tblModel.addRow(columns);
                
            }            
        }catch(SQLException e){
            System.out.println("Masalah Terdapat pada QUERY / WEB SERVER");
        }
    }
